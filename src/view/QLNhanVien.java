    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(checkrong()){
            NhanVien nv = readform();
            if(service.insert(nv)!=0){
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                fillTable(service.getAll());
        }else{
           JOptionPane.showMessageDialog(this, "Thêm không thành công"); 
        }       }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        index = tblNhanVien.getSelectedRow();
        String x = tblNhanVien.getValueAt(index, 0).toString();
        if (service.delete(x) != 0) {
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            fillTable(service.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (checkrong()) {
            index = tblNhanVien.getSelectedRow();
            NhanVien nv = readform();
            String id = tblNhanVien.getValueAt(index, 0).toString();
            if (service.update(nv, id) > 0) {
                JOptionPane.showMessageDialog(this, "Update thành công");
                fillTable(service.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "Update không thành công");
            }
        }

    }//GEN-LAST:event_btnSuaActionPerformed

