    private void fillTable(List<NhanVien> all) {
        dtm = (DefaultTableModel) tblNhanVien.getModel();
        dtm.setRowCount(0);
        for (NhanVien s : all) {
            dtm.addRow(s.toData());
        }
    }

    private void showData(int index) {
        NhanVien nv = service.getAll().get(index);
        txtID.setText(nv.getId());
        txtHoTen.setText(nv.getHoTen());
        txtEmail.setText(nv.getEmail());
        txtMatKhau.setText(nv.getMatKhau());
        txtTinhTrang.setText(nv.getTinhTrang() + "");
        txtVaiTro.setText(nv.getVaiTro() + "");

        tblNhanVien.setRowSelectionInterval(index, index);
    }

    boolean checkrong() {
        if (txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "ID Không được để mã trống");
            txtID.requestFocus();
            return false;
        }
        if (txtHoTen.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Họ tên Không được để tên trống");
            txtHoTen.requestFocus();
            return false;
        }
        if (txtEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Email Không được để mật khẩu trống");
            txtEmail.requestFocus();
            return false;
        }
        if (txtMatKhau.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Mật khẩu Không được để mật khẩu trống");
            txtMatKhau.requestFocus();
            return false;
        }
        if (txtTinhTrang.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Tình Trạng Không được để mật khẩu trống");
            txtTinhTrang.requestFocus();
            return false;
        }
        if (txtVaiTro.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vai Trò Không được để mật khẩu trống");
            txtVaiTro.requestFocus();
            return false;
        }
        return true;
    }

    public NhanVien readform() {
        String ID = txtID.getText();
        String HoTen = txtHoTen.getText();
        String Email = txtEmail.getText();
        String MatKhau = txtMatKhau.getText();
        int TinhTrang = Integer.parseInt(txtTinhTrang.getText());
        int VaiTro = Integer.parseInt(txtVaiTro.getText());
        return new NhanVien(ID, MatKhau, HoTen, Email, TinhTrang, VaiTro);
    }

